# --------------------------------
# p-value formatting
# --------------------------------

round_half_up <- function(x, digits = 0) {
  pos <- 10^digits
  floor(x * pos + 0.5) / pos
}

format_p <- function(p) {
  if (is.na(p)) return(NA_character_)
  if (p < 0.001) return("<0.001")
  decimals <- if (p >= 0.1) 1 else max(0, -floor(log10(p)))
  rounded <- round_half_up(p, digits = decimals)
  sprintf(paste0("%.", decimals, "f"), rounded)
}
